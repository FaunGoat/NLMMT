// @route GET /api/products
// @desc Get all products with optional query filters
// @access Public
router.get("/", async (req, res) => {
  try {
    const {
      collection,
      size,
      color,
      minPrice,
      maxPrice,
      sortBy,
      search,
      normalizedSearch,
      category,
      material,
      limit,
    } = req.query;

    let query = {};

    // Filter logic
    if (collection && collection.toLowerCase() !== "all") {
      query.collections = collection;
    }

    if (category && category.toLowerCase() !== "all") {
      query.category = category;
    }

    if (material) {
      query.material = { $in: material.split(",") };
    }

    if (size) {
      query.sizes = { $in: size.split(",") };
    }

    if (color) {
      query.colors = { $in: [color] };
    }

    if (minPrice || maxPrice) {
      query.price = {};
      if (minPrice) query.price.$gte = Number(minPrice);
      if (maxPrice) query.price.$lte = Number(maxPrice);
    }

    // Search logic
    if (search || normalizedSearch) {
      const searchTerm = normalizedSearch || normalizeString(search);
      // console.log("Search Term:", searchTerm); // Debug

      // Tách searchTerm thành mảng các từ riêng lẻ
      const searchWords = searchTerm.split(" ").filter((word) => word);

      // Tạo điều kiện tìm kiếm cho từng từ trên nhiều trường
      query.$or = [
        ...searchWords.map((word) => ({
          name: { $regex: word, $options: "i" },
        })),
        ...searchWords.map((word) => ({
          description: { $regex: word, $options: "i" },
        })),
        ...searchWords.map((word) => ({
          category: { $regex: word, $options: "i" },
        })),
        ...searchWords.map((word) => ({
          material: { $regex: word, $options: "i" },
        })),
      ];
    }

    // Sort logic
    let sort = {};
    if (sortBy) {
      switch (sortBy) {
        case "priceAsc":
          sort = { price: 1 };
          break;
        case "priceDesc":
          sort = { price: -1 };
          break;
        case "popularity":
          sort = { rating: -1 };
          break;
        default:
          break;
      }
    }

    // Fetch products
    const products = await Product.find(query)
      .sort(sort)
      .limit(Number(limit) || 0);

    // console.log("Products found:", products); // Debug
    res.json(products);
  } catch (error) {
    console.error(error);
    res.status(500).send("Server Error");
  }
});

